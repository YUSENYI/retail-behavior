def replace_paragraph_text(para, new_text):
    """保留第一个 run 的格式，清空所有 run 后写入新文本"""
    if not para.runs:
        return
    # 保存第一个 run 的格式
    first_run = para.runs[0]
    font_name = first_run.font.name
    font_size = first_run.font.size
    font_bold = first_run.font.bold
    font_color = first_run.font.color.rgb if first_run.font.color and first_run.font.color.rgb else None
    
    # 清空段落
    para.clear()
    
    # 添加新 run
    new_run = para.add_run(new_text)
    if font_name:
        new_run.font.name = font_name
        new_run._element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
    if font_size:
        new_run.font.size = font_size
    if font_bold is not None:
        new_run.font.bold = font_bold
    if font_color:
        new_run.font.color.rgb = font_color

def modify_docx(src_path, dst_filename, para_mapping):
    """复制原文件并修改文本"""
    dst_path = os.path.join(OUT_DIR, dst_filename)
    # 如果目标已存在，先删除
    if os.path.exists(dst_path):
        os.remove(dst_path)
    # 复制原文件
    shutil.copy2(src_path, dst_path)
    
    # 打开副本修改
    doc = Document(dst_path)
    
    for idx, change in para_mapping.items():
        if idx >= len(doc.paragraphs):
            print(f"  WARNING: para {idx} out of range (total {len(doc.paragraphs)})")
            continue
        
        para = doc.paragraphs[idx]
        
        if isinstance(change, dict):
            # 修改特定 run
            for run_idx, run_text in change.items():
                if run_idx < len(para.runs):
                    # 只替换该 run 的文本，保留格式
                    para.runs[run_idx].text = run_text
        elif isinstance(change, str):
            # 替换整个段落文本
            replace_paragraph_text(para, change)
    
    doc.save(dst_path)
    print(f"  -> {dst_filename} 生成完成")

